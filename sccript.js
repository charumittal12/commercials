//vid1
let clip1 = document.querySelector(".vid1");
// Play the video when the mouse hovers over it
clip1.addEventListener("mouseover", function (e) {
	clip1.muted=true;
    clip1.play();
});
// Stop the video when the mouse is out of the video
clip1.addEventListener("mouseout", function (e) {
    clip1.pause();
});

